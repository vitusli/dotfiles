import bpy, addon_utils

def get_addon_version():
    return str(
        [
            addon.bl_info.get("version", (-1, -1, -1)) 
            for addon in addon_utils.modules() 
            if addon.bl_info["name"] == 'Render Raw'
        ][0]
    )